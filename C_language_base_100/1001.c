/*
  C언어에서 가장 기본적인 명령이 출력문이다.
  printf()를 이용해 단어를 출력해보자.
*/

#include <stdio.h>
int main(int argc, char** argv) {
  printf("Hello\n");
  return 0;
}

/*
  자주 하는 실수 / 참고
  1. printf에서 작은 따옴표(') 사용 실수
  printf('Hello\n'); // 컴파일 오류 발생
  printf에서 문자열을 출력할 때는 큰 따옴표(")를
  사용해야 함. 작은 따옴표를 사용하면, 문자 리터럴(char)이
  아니라 여러 개의 문자로 이루어진 문자열을 표현하려는
  것이므로 오류가 발생함.
  2. #include <stdio.h> 누락
  위를 선언하지 않으면 printf 함수가 정의되지
  않아 컴파일 오류가 발생함.
  3. 채점 특징
  gets 함수는 보안성 때문에 C11부터
  비표준 함수가 되었다. 하지만 코드업은 C99 표준을
  따르고 있으므로 gets 함수가 사용 가능하다.
  4. printf 호출 시 세미콜론(;) 누락
  C언어에서는 문장이 끝날 때 반드시 세미콜론(;)을 붙여야 한다.
  5. main의 반환 타입을 void로 선언
  C 표준에서 main의 반환 타입은 int이다. void main()은
  컴파일러에 따라 경고 또는 오류가 발생할 수 있다.
  6. return 0;을 생략 (경고 가능)
  C 표준에서는 main 함수가 int를 반환해야 하므로
  return 0;을 명시하는 것이 좋다. 대부분의 컴파일러에서는
  이를 자동으로 추가하지만 명확한 코드 작성을 위해
  명시하는 것이 좋다.
*/
